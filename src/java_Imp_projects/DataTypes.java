package java_Imp_projects;

public class DataTypes {
	//All Data Types
	public static void main(String[] args) {
		//Integer Data types
		byte b= 8;
		System.out.println(b);
		short s = 16;
		System.out.println(s);
		int i = 320;
		System.out.println(i);
		long l = 64000;
		System.out.println(l);
		//Decimal& Float Data types
		float fl = 24.34f;
		System.out.println(fl);
		double d = 234.56;
		System.out.println(d);
		//Char types
		char c = '4';
		System.out.println(c);
		//Conditional types true or false
		boolean t = true;
		System.out.println(t);
		//String = It is not a data type.It is Predefined class in java
		String S ="Hello";
		System.out.println(S);

	}

}
